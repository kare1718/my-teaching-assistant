function randomScore(max, min = 0) {
  const u1 = Math.random();
  const u2 = Math.random();
  const z = Math.sqrt(-2 * Math.log(u1)) * Math.cos(2 * Math.PI * u2);
  const mean = max * 0.65;
  const stdDev = max * 0.15;
  let score = mean + z * stdDev;
  score = Math.max(min, Math.min(max, score));
  return Math.round(score * 10) / 10;
}

function calculateRanks(examId) {
  const scores = getAll('SELECT id, score FROM scores WHERE exam_id = ? ORDER BY score DESC', [examId]);
  if (scores.length === 0) return;
  let rank = 1;
  for (let i = 0; i < scores.length; i++) {
    if (i > 0 && scores[i].score < scores[i - 1].score) {
      rank = i + 1;
    }
    runQuery('UPDATE scores SET rank_num = ? WHERE id = ?', [rank, scores[i].id]);
  }
}

