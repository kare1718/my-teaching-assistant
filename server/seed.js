function buildStudents() {
  const students = [];
  const schools = Object.keys(NAMES);
  const prefixes = { 'A고등학교': 'sa', 'B고등학교': 'sb', 'C고등학교': 'sc', 'D고등학교': 'sd', '중3': 'm3' };
  for (const school of schools) {
    const names = NAMES[school];
    for (let i = 0; i < names.length; i++) {
      const num = i + 1;
      const prefix = prefixes[school];
      students.push({
        username: `student_${prefix}${num}`,
        name: names[i],
        school,
        grade: i < 10 ? '1학년' : '2학년',
        parentName: `${names[i].charAt(0)}학부모`,
        parentPhone: `010-${String(schools.indexOf(school)+1).repeat(4)}-${String(num).padStart(4,'0')}`,
      });
    }
  }
  return students;
}

